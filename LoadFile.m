tableout.Varighedm1medarbejder = cellstr(stringVectors(:,4));
tableout.Varighedm0medarbejder = data(:,6);
tableout.Arbejdsgang = categorical(stringVectors(:,5));
tableout.Medarbejdere = data(:,7);
tableout.Tidmedborger = cellstr(stringVectors(:,6));
tableout.Superbruger = data(:,8);
tableout.Almindeligtpersonale = data(:,9);
tableout.Aflser = data(:,10);

tableout.Varighedforarbejdsgang  = datetime(tableout.Varighedforarbejdsgang(:),'InputFormat','MM:SS:FFF');
tableout.Varighedforarbejdsgang.Format = 'MM:SS';
